"""Example of use underscore"""
for _ in range(5): # Using underscore as a throwaway variable
    print('Hello, World!')

list_ = [1, 2, 3, 4, 5]
print(list_)  # Using underscore to avoid conflict with built-in name 'list'

NUMBER = 1_148_328
print(NUMBER)  # Using underscore as a digit separator for readability
